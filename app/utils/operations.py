"""Basic operations"""


def sum_numbers(num1: int, num2: int):
    """Function to sum two numbers

    Args:
        num1 (int) 
        num2 (int)

    Returns:
        result of num1 + num2
    """
    result = num1 + num2
    return result


def substract_numbers(num1: int, num2: int):
    """Function to substract two numbers

    Args:
        num1 (int)
        num2 (int)

    Returns:
        result of num1 - num2
    """
    result = num1 - num2
    return result


def multiply_numbers(num1: int, num2: int):
    """Function to multiply two numbers

      Args:
          num1 (int)
          num2 (int)

      Returns:
          result of num1 * num2
      """
    result = num1 * num2
    return result


def divide_numbers(num1: int, num2: int):
    """Function to divide two numbers

    Args:
        num1 (int)
        num2 (int)

    Returns:
        result of num1 / num2
    """
    result = num1 / num2
    return result
